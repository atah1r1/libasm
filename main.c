#include <stdio.h>
#include <string.h>

size_t		ft_strlen(char const *str);
char        *ft_strcpy(char *dest, char *src);

void clear_buffer(char *buffer, int size)
{
	int i = 0;
	while (i < size)
		buffer[i++] = 0;
}

